const calculateArea = function(width, height) {
    return width * height;
};

let area1 = calculateArea(5, 10); // Викликаємо функцію
console.log(`Площа прямокутника: ${area1}`); // Виводимо результат



const calculateAreaArrow = (width, height) => width * height;

let area2 = calculateAreaArrow(5, 10); // Викликаємо функцію
console.log(`Площа прямокутника: ${area2}`); // Виводимо результат

