const calculateArea = function(width, height) {
    return width * height;
};

let area1 = calculateArea(5, 10);
console.log(`Площа прямокутника: ${area1}`);



const calculateAreaArrow = (width, height) => width * height;

let area2 = calculateAreaArrow(5, 10); 
console.log(`Площа прямокутника: ${area2}`);

